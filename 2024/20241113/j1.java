package midterm1113;

public class j1 {

    public static void main(String[] args) {

        int integerVar = 10;
        char charVar = 'A';
        float floatVar = 3.45f;
        double doubleVar = 2.2748211;

        System.out.printf("Integer: %d%n", integerVar);
        System.out.printf("Character: %c%n", charVar);
        System.out.printf("Float: %.2f%n", floatVar);
        System.out.printf("Double: %.5f%n", doubleVar);
    }
}
